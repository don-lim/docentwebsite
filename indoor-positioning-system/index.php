				<h2>An Introduction to IPS</h2>
            	<P>What does an IPS (Indoor Positioning System) do? People use positioning systems such as GPS on an hourly basis without realizing it. However, GPS doesn’t work inside a building and does not provide enough accuracy to be useful in indoor venues where things are very close to each other. Indoor Location Based Services will help customers find products and services quickly and easily.<br><br>
				In an indoor setting, high accuracy is the key. An Indoor Positioning System must provide much greater accuracy compared to GPS, which is used over long distances or areas.<br><br>
 
				<h3>A General Rule of Thumb</h3>
				In order to implement a positioning system, you must have several reference points with a known location or a known path of movement. In the case of GPS, there are satellites with a known trajectory orbiting the earth. For indoor locations, you need to have some kind of device installed in the building that provides reference points (the exception is photographic positioning, which mimics the complex processes of the human brain). There is no way you can know your position without reference points. Most IPS’s use devices that utilize radio frequency signals at a fixed location to provide your position on a map.<br><br>
				<h3>Alternative Methods</h3>
				Photographic methods mimicking the human brain are very expensive and require knowledge of the landscape. They require a large database of images. Maintaining the system is also expensive. You basically need to photograph every single corner of your building periodically. It will never work in locations such as parking lots where the landscape changes very frequently.<br><br>
				Positioning someone on a particular floor is a major challenge for most IPS’s. This is mainly due to the big margin of error of radio frequency based IPS’s. However, IPS’s that use non-radio frequency methods are very expensive to implement and maintain.<br><br>
 
